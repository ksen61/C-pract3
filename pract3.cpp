#include <iostream>
#include <vector>
#include <Windows.h>
using namespace std;

int main()
{
    setlocale(LC_ALL, "Russian");
    const int rows = 3;
    const int cols = 5;

    // Ввод двумерного массива
    vector<vector<double>> matrix(rows, vector<double>(cols));
    for (int i = 0; i < rows; ++i) {
        cout << "Введите элементы " << i + 1 << "-й строки (через пробел): ";
        for (int j = 0; j < cols; ++j) {
            cin >> matrix[i][j];
        }
    }

    // Вычисление среднего арифметического элементов строк
    vector<double> averages(rows);
    for (int i = 0; i < rows; ++i) {
        double sum = 0;
        for (int j = 0; j < cols; ++j) {
            sum += matrix[i][j];
        }
        averages[i] = sum / cols;
    }

    // Вывод результатов
    for (int i = 0; i < rows; ++i) {
        cout << "Среднее арифметическое элементов строки " << i + 1 << ": " << averages[i] << endl;
    }

    return 0;
}
